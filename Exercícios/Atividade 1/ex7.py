""""
Faça um Programa que pergunte quanto você ganha por hora e o
número de horas trabalhadas no mês. Calcule e mostre o total do
seu salário no referido mês.

"""

ganhoHr = float(input("Quanto você ganha por hora?"))
horasMes = float(input("Quantas horas você trabalha por mês?"))

salarioTotal = ganhoHr * horasMes

print(f"Seu salário total no mês é de R$ {salarioTotal}")

